    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        if (isStaffEmpty()) {
            String nameAndSurname = txtName.getText();
            String cellNo = txtCellNo.getText();
            String email = txtEmail.getText();
            String homeAddress = txtHomeAddress.getText();
            String id = txtID.getText();
            String position = cbPosition.getSelectedItem().toString();
            

            String sql = "INSERT INTO staff (`name_&_surname`, `cellphone_number`, `email_address`, `home_address`, `id_number`, `position`) VALUES (?, ?, ?, ?, ?, ?)";

            try (Connection conn = DatabaseConnection.getConnection(); PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, nameAndSurname);
                pstmt.setString(2, cellNo);
                pstmt.setString(3, email);
                pstmt.setString(4, homeAddress);
                pstmt.setString(5, id);
                pstmt.setString(6, position);
                

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data inserted successfully");
                clearText();
                populateStaffTable();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error inserting data");
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

