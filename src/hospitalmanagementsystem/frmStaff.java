    public void clearText(){
        txtCellNo.setText("");
        txtEmail.setText("");
        txtHomeAddress.setText("");
        txtName.setText("");
        txtID.setText("");
        cbPosition.setSelectedIndex(0);
    }
    
    public boolean isStaffEmpty() {
        
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Staff Member's Name and Surname Are Missing");
            return false;
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Staff Member's Email Is Missing");
            return false;
        } else if (!txtEmail.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid Email Address");
            return false;
        } else if (txtCellNo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Staff Member's Phone Number Is Missing");
            return false;
        } else if (txtCellNo.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Staff Member's Phone Number Is Too Long");
            return false;
        } else if (txtHomeAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Staff Member's Home Address Is Missing");
            return false;
        } else if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Staff Member's ID Number Is Missing");
            return false;
        } else if (txtID.getText().length() > 13) {
            JOptionPane.showMessageDialog(this, "Staff Member's ID Number Is Too Long");
            return false;
        } 

        return true;
    }
    
    private void populateStaffTable() {
        DefaultTableModel model = (DefaultTableModel) tableStaff.getModel();
        model.setRowCount(0); // Clear the existing rows

        String sql = "SELECT `name_&_surname`, `cellphone_number`, `email_address`, `home_address`, `id_number`, `position` FROM staff";

        try (Connection conn = DatabaseConnection.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                Vector<String> row = new Vector<>();
                row.add(rs.getString("name_&_surname"));
                row.add(rs.getString("cellphone_number"));
                row.add(rs.getString("email_address"));
                row.add(rs.getString("home_address"));
                row.add(rs.getString("id_number"));
                row.add(rs.getString("position"));
                model.addRow(row);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error retrieving data");
        }
    }
    
