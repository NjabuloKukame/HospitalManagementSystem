    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        if (isPatientEmpty()) {
            String nameAndSurname = txtName.getText();
            String cellNo = txtCellNo.getText();
            String email = txtEmail.getText();
            String homeAddress = txtHomeAddress.getText();
            String id = txtID.getText();
            String condition = txtCondition.getText();
            String date = txtTime.getText();

            String sql = "INSERT INTO patients (`name_&_surname`, `cellphone_number`, `email_address`, `home_address`, `id_number`, `condition`, `time`) VALUES (?, ?, ?, ?, ?, ?, ?)";

            try (Connection conn = DatabaseConnection.getConnection(); PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, nameAndSurname);
                pstmt.setString(2, cellNo);
                pstmt.setString(3, email);
                pstmt.setString(4, homeAddress);
                pstmt.setString(5, id);
                pstmt.setString(6, condition);
                pstmt.setString(7, date);

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data inserted successfully");
                clearText();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error inserting data");
            }
        }

    }//GEN-LAST:event_btnInsertActionPerformed

