    public boolean isPatientEmpty() {
        
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Patient's Name and Surname Are Missing");
            return false;
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Patient's Email Is Missing");
            return false;
        } else if (!txtEmail.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid Email Address");
            return false;
        } else if (txtCellNo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Patient's Phone Number Is Missing");
            return false;
        } else if (txtCellNo.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Patient's Phone Number Is Too Long");
            return false;
        } else if (txtHomeAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Patient's Home Address Is Missing");
            return false;
        } else if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Patient's ID Number Is Missing");
            return false;
        } else if (txtID.getText().length() >= 13) {
            JOptionPane.showMessageDialog(this, "Patient's ID Number Is Too Long");
            return false;
        } else if (txtCondition.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Patient's Condition Is Missing");
            return false;
        }

        return true;
    }

