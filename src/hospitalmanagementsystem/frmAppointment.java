    private void btnSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetActionPerformed
        // TODO add your handling code here:
        if(isAppointmentEmpty()){
            String nameAndSurname = txtName.getText();
            String cellNo = txtCellNo.getText();
            String id = txtID.getText();
            String date = txtTime.getText();
            

            String sql = "INSERT INTO appointment (`name_&_surname`, `cellphone_number`, `id_number`, `date`) VALUES (?, ?, ?, ?)";

            try (Connection conn = DatabaseConnection.getConnection(); PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, nameAndSurname);
                pstmt.setString(2, cellNo);
                pstmt.setString(3, id);
                pstmt.setString(4, date);
                

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data inserted successfully");
                clearText();
                populateAppointmentTable();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error inserting data");
            }
        }
    }//GEN-LAST:event_btnSetActionPerformed

    private void populateAppointmentTable() {
        DefaultTableModel model = (DefaultTableModel) tableAppointment.getModel();
        model.setRowCount(0); // Clear the existing rows

        String sql = "SELECT `name_&_surname`, `cellphone_number`, `id_number`, `date` FROM appointment";

        try (Connection conn = DatabaseConnection.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                Vector<String> row = new Vector<>();
                row.add(rs.getString("name_&_surname"));
                row.add(rs.getString("cellphone_number"));
                row.add(rs.getString("id_number"));
                row.add(rs.getString("date"));
                model.addRow(row);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error retrieving data");
        }
    }
    
