    public boolean isAppointmentEmpty(){
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Potential Client's Name and Surname Are Missing");
            return false;
        }else if (txtCellNo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Potential Client's Phone Number Is Missing");
            return false;
        } else if (txtCellNo.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Potential Client's Phone Number Is Too Long");
            return false;
        } else if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Potential Client's ID Number Is Missing");
            return false;
        } else if (txtID.getText().length() > 13) {
            JOptionPane.showMessageDialog(this, "Potential Client's ID Number Is Too Long");
            return false;
        } else if (txtTime.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Potential Client's Appointment Date Is Missing");
            return false;
        }else if (!isValidDate(txtTime.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Date. Please Enter The Correct Format(yyyy-MM-dd)");
            return false;
        }
        
        return true;
    }
    
    public static boolean isValidDate(String dateStr) {
        DateTimeFormatter dateFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        try {
            // Parse the date string to LocalDate
            LocalDate date = LocalDate.parse(dateStr, dateFormatter);
            
            // Get the current date
            LocalDate currentDate = LocalDate.now();
            
            // Check if the date is before the current date
            if (date.isBefore(currentDate)) {
                return false;
            }
        } catch (DateTimeParseException e) {
            // If parsing fails, the format is incorrect
            return false;
        }
        
        // If all checks pass, the date is valid
        return true;
    }
